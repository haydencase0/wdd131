const grid = document.querySelector('.grid');

let currentIndex = 0;
const batchSize = 15;
let isLoading = false;
let currentFilter = null;

const masonry = new Masonry(grid, {
    itemSelector: ".grid-item",
    percentPosition: true,
    gutter: 10
});

function loadGallery(filterTag = null) {
    currentFilter = filterTag;
    currentIndex = 0;

    grid.innerHTML = "";
    masonry.reloadItems();
    masonry.layout();

    loadMoreImages();
}

const modal = document.getElementById("modal");
const modalImg = document.getElementById("modal-img");
const modalCaption = document.getElementById("modal-caption");
const closeBtn = document.getElementById("close");

function openModal(src, caption) {
    modal.classList.add("active");
    modalImg.src = src;
    modalCaption.textContent = caption;
}

closeBtn.addEventListener("click", () => {
    modal.classList.remove("active");
});

modal.addEventListener("click", (e) => {
    if (e.target === modal) {
        modal.classList.remove("active");
    }
});

function loadMoreImages() {
    if (isLoading) return;
    isLoading = true;

    const filtered = currentFilter
        ? gallery.filter(item => item.tags.includes(currentFilter))
        : gallery;

    const nextBatch = filtered.slice(currentIndex, currentIndex + batchSize);

    if (nextBatch.length === 0) {
        isLoading = false;
        return;
    }

    nextBatch.forEach(itemData => {
        const item = document.createElement("div");
        item.classList.add("grid-item");
        
        const img = document.createElement("img");
        img.src = itemData.image.replace(
            "/upload/",
            "/upload/w_500,q_auto,f_auto/"
        );

        img.loading = "lazy";        

        img.addEventListener("click", () => {
            openModal(itemData.image, itemData.description);
        });

        item.appendChild(img);
        grid.appendChild(item);

        masonry.appended(item);
    });

    currentIndex += batchSize;

    imagesLoaded(grid, function () {
        masonry.layout();
        isLoading = false;
    });
}

window.addEventListener("scroll", () => {
    const scrollPosition = window.innerHeight + window.scrollY;
    const pageHeight = document.body.offsetHeight;

    if (scrollPosition >= pageHeight - 100) {
        loadMoreImages();
    }
});

const page = window.location.pathname;

document.addEventListener("DOMContentLoaded", () => {
    const path = window.location.pathname;

    if (path.includes("people")) {
        loadGallery("People");
    } else if (path.includes("food")) {
        loadGallery("Food");
    } else if (path.includes("scenery")) {
        loadGallery("Places");
    } else {
        loadGallery();
    }
});
